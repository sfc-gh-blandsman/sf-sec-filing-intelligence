    SIC_CODE        VARCHAR(4)     NOT NULL PRIMARY KEY,
    OFFICE          VARCHAR(100)   NOT NULL,
    INDUSTRY_TITLE  VARCHAR(200)   NOT NULL,
    SECTOR          VARCHAR(100)   NOT NULL
)
COMMENT = 'SEC Standard Industrial Classification codes with Office assignment. Source of truth for INDUSTRY_SECTOR + INDUSTRY_TITLE mapping.';

