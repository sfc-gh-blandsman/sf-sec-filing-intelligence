-- =============================================================================
-- 00: SIC Reference Data
-- =============================================================================
-- Creates and populates the SIC_CODES reference table with SEC's official
-- Standard Industrial Classification codes mapped to SEC Division of
-- Corporation Finance Office assignments.
--
-- Source: https://www.sec.gov/corpfin/division-of-corporation-finance-standard-industrial-classification-sic-code-list
--
-- Schema:
--   SIC_CODE       — 4-digit SEC code
--   OFFICE         — Full SEC Office name (e.g., "Office of Technology")
--   INDUSTRY_TITLE — Specific industry description (~444 values)
--   SECTOR         — Stripped Office prefix (~10 values, used as primary dimension)
--
-- This table is the source of truth for industry classification.
-- Used by: 02_metadata_backfill.sql (JOIN to populate INDUSTRY_SECTOR + INDUSTRY_TITLE)
--
-- Run 00_config.sql first to set session variables.
-- =============================================================================

USE DATABASE IDENTIFIER($config_database);
USE SCHEMA IDENTIFIER($config_schema);
USE WAREHOUSE IDENTIFIER($config_warehouse);

-- =============================================================================
-- Reference Table
-- =============================================================================

